def scaffold_readme(plugin_id: str, tool_name: str, output_files: list[str]) -> str:
    file_list = "\n".join(f"- `{name}`" for name in output_files)
    return f"""# Capability-Free Starter Plugin Scaffold

This directory was generated for:

- plugin id: `{plugin_id}`
- tool name: `{tool_name}`
- authoring class: `capability_free_local_deterministic`

It is a bounded scaffold, not a finished plugin.

Generated files:

{file_list}

How to use it:

1. copy the runtime snippet into
   `crates/psionic-runtime/src/tassadar_post_article_starter_plugin_runtime.rs`
2. replace every `TODO` with plugin-specific logic and negative claims
3. copy the example bundle writer into `crates/psionic-runtime/examples/`
4. merge the test stub into the runtime test module
5. copy the checker script into `scripts/`
6. decide explicitly whether the plugin should stay runtime-only or later be
   admitted to bridge, catalog, and controller surfaces

What it does not do:

- no automatic runtime-file patching
- no networked plugin scaffolding
- no automatic bridge, catalog, or controller admission
- no public publication or external binary support
"""


def main() -> None:
    args = parse_args()
    parts = validate_plugin_id(args.plugin_id)
    if args.authoring_class != "capability_free_local_deterministic":
        raise SystemExit(
            "networked_read_only is intentionally not scaffolded; "
            "networked starter plugins must keep mount, replay, and policy truth "
            "explicit through manual authoring"
        )
    name_parts = parts[1:]
    snake = "_".join(name_parts)
    tool_name = f"plugin_{snake}"
    const_slug = "_".join(part.upper() for part in name_parts)
    pascal = "".join(part.capitalize() for part in name_parts)
    schema_base = args.plugin_id
    example_file_name = snake
    output_dir = Path(args.output_dir)

    output_files = [
        "README.md",
        "scaffold_manifest.json",
        f"plugin_{snake}_runtime_snippet.rs",
        f"tassadar_post_article_{snake}_bundle.rs",
        f"plugin_{snake}_tests.rs",
        f"check-{snake}.sh",
    ]

    write(
        output_dir / "README.md",
        scaffold_readme(args.plugin_id, tool_name, output_files),
    )
    write(
        output_dir / "scaffold_manifest.json",
        manifest(args.plugin_id, tool_name, args.authoring_class, output_files),
    )
    write(
        output_dir / f"plugin_{snake}_runtime_snippet.rs",
        runtime_snippet(
            plugin_id=args.plugin_id,
            tool_name=tool_name,
            const_slug=const_slug,
            schema_base=schema_base,
            mount_envelope_id=f"mount.{args.plugin_id}.no_capabilities.v1",
            runtime_bundle_id=f"tassadar.post_article.{tool_name}.runtime_bundle.v1",
            runtime_bundle_ref=(
                f"fixtures/tassadar/runs/tassadar_post_article_{snake}_v1/"
                f"tassadar_post_article_{snake}_bundle.json"
            ),
            runtime_run_root_ref=(
                f"fixtures/tassadar/runs/tassadar_post_article_{snake}_v1"
            ),
            manifest_id=f"manifest.{args.plugin_id}.v1",
            artifact_id=f"artifact.{args.plugin_id}.v1",
            type_name=pascal,
            snake=snake,
        ),
    )
    write(
        output_dir / f"tassadar_post_article_{snake}_bundle.rs",
        example_bundle_writer(snake, example_file_name),
    )
    write(
        output_dir / f"plugin_{snake}_tests.rs",
        test_snippet(snake, example_file_name),
    )
    write(
        output_dir / f"check-{snake}.sh",
        checker_script(example_file_name, pascal, tool_name),
    )


if __name__ == "__main__":
    main()
