from __future__ import annotations

import argparse
import json
from pathlib import Path


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Generate a bounded starter-plugin scaffold for the capability-free "
            "local deterministic class."
        )
    )
    parser.add_argument(
        "--plugin-id",
        required=True,
        help="Plugin id shaped like plugin.<domain>.<name>",
    )
    parser.add_argument(
        "--authoring-class",
        required=True,
        choices=["capability_free_local_deterministic", "networked_read_only"],
        help="Starter-plugin authoring class to scaffold or refuse explicitly",
    )
    parser.add_argument(
        "--output-dir",
        required=True,
        help="Directory where the scaffold files should be written",
    )
    return parser.parse_args()


def validate_plugin_id(plugin_id: str) -> list[str]:
    parts = plugin_id.split(".")
    if len(parts) < 3 or parts[0] != "plugin" or any(not part for part in parts):
        raise SystemExit(
            "--plugin-id must be shaped like plugin.<domain>.<name> with no empty segments"
        )
    return parts


def write(path: Path, contents: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(contents, encoding="utf-8")


def manifest(
    plugin_id: str, tool_name: str, authoring_class: str, output_files: list[str]
) -> str:
    payload = {
        "schema_version": 1,
        "plugin_id": plugin_id,
        "tool_name": tool_name,
        "authoring_class": authoring_class,
        "bridge_exposed_default": False,
        "catalog_exposed_default": False,
        "generated_files": output_files,
    }
    return json.dumps(payload, indent=2) + "\n"


